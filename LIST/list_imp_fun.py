# len Function 


l = [10,20,40,49,50]
print(len(l))


# Count Function

l = [10,20,30,40,20,40,10,50,30]
print(l.count(10))
print(l.count(30))
print(l.count(50))


# Index Function 

l = [10,20,30,40,20,40,10,50,30]

print(l.index(30))
print(l.index(50))
print(l.index(20))
print(l.index(90)) # ValueError : 90 is not in list