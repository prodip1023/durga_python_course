
# Multivalues Tuple


t = 10,20,30,40
print(t)
print(type(t))


# Creation of Tuple

t = ()
print(type(t))


# Not Tuple

t = (10)
print(type(t))

# Single value Tuple 

t1 = (10,)
print(type(t1))



# By using tuple() Function 

list = [10,20,30,40]
t = tuple(list)
print(t)


t = tuple(range(10,20,2))
print(t)